# use the range() function to generate a series of numbers
for value in range(0, 10):
    print(value)
    
print('\n')

# make a list of numbers by converting the results of range() into a list using the list() function
numbers = list(range(1, 8))

print(numbers)

print('\n')

# simple statistics with a list of numbers
print(min(range(0, 8)))

print(max(range(0, 8)))

print(sum(range(0, 8)))